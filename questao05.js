//A variavel let é uma variavel mutavel, sendo assim posso altera-la futuramente no codigo:
let idade = 15

console.log(idade)

//vamos supor que a pessoa fez aniversario entao:
idade = idade + 1

console.log(idade)

//Ja uma variavel const é imutavel, ou seja não posso altera-la
